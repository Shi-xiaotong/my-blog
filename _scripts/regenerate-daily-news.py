DESCRIPTIONS = {k: v[0][:150] for k, v in ARTICLES.items()}


def generate_article(date_str):
    date_obj = datetime.strptime(date_str, "%Y-%m-%d")
    date_display = date_obj.strftime("%Y年%m月%d日")
    title = TITLES.get(date_str, f"{date_display} AI 资讯")
    opening, sections, closing = ARTICLES.get(date_str, ("今日 AI 资讯。", [], ""))
    desc = DESCRIPTIONS.get(date_str, "")

    # Build body
    body_parts = [opening]
    for heading, *paragraphs in sections:
        body_parts.append(f"## {heading}\n")
        for p in paragraphs:
            body_parts.append(p + "\n")
    body_parts.append("---\n")
    body_parts.append(f"以上就是 {date_display} 的 AI 资讯精选。懂技术的聊技术，不懂技术的看热闹——关注「水星引力m」，每天带你看点不一样的。\n")
    body_parts.append(f"> *📮 欢迎在评论区分享你的看法。如果觉得内容有帮助，不妨分享给朋友。*\n")

    body = "\n".join(body_parts)

    # Insert <!-- more --> after first paragraph
    paras = body.split('\n\n')
    if len(paras) > 2:
        body = paras[0] + '\n\n<!-- more -->\n\n' + '\n\n'.join(paras[1:])

    # Word count check
    word_count = len(body.replace('\n', '').replace(' ', ''))
    print(f"  {date_str}: ~{word_count} chars")

    def yaml_quote(s):
        if '"' in s:
            return "'" + s.replace("'", "''") + "'"
        return f"\"{s}\""

    frontmatter = [
        "---",
        f"title: {yaml_quote(title)}",
        f"date: {date_str} 12:00:00",
        "categories:",
        "  - daily-news",
        "tags:",
        "  - 每日热点",
        f"description: {yaml_quote(desc)}" if desc else "",
        "---",
    ]
    frontmatter = [l for l in frontmatter if l]

    md = "\n".join(frontmatter) + "\n\n" + body + "\n"

    post_dir = os.path.join(BLOG_DIR, "source", "_posts", "daily-news")
    os.makedirs(post_dir, exist_ok=True)
    path = os.path.join(post_dir, f"{date_str}-daily-hotspot.md")
    with open(path, "w", encoding="utf-8") as f:
        f.write(md)
    return path


def main():
    start = datetime(2026, 6, 1)
    end = datetime(2026, 7, 8)
    current = start
    count = 0
    while current <= end:
        date_str = current.strftime("%Y-%m-%d")
        path = generate_article(date_str)
        count += 1
        current += timedelta(days=1)
    print(f"\nDone. {count} articles generated.")


if __name__ == "__main__":
    main()
